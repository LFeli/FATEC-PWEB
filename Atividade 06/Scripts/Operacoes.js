var n1, n2;

n1 = prompt("Digite o primeiro número: ");
n2 = prompt("Digite o segundo número: ");

alert("Soma dos números: " + (parseFloat(n1) + parseFloat(n2)));
alert("Subtração do primeiro pelo segundo: " + (parseFloat(n1) - parseFloat(n2)));
alert("Produto dos números: " + (parseFloat(n1) * parseFloat(n2)));
alert("Divisão do primeiro pelo segundo: " + (parseFloat(n1) / parseFloat(n2)));
alert("Resto da divisão do primeiro pelo segundo: " + (parseFloat(n1) % parseFloat(n2)));